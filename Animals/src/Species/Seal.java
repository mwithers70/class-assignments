package Species;

public class Seal extends Animal{
//    String name = "Seal named Earl";

    public Seal(String name) { super(name); }

    @Override
    public void animalInfo (){
        System.out.println("I am a " +name+ "." + "\n" + "I belong to the " +phylum+ " phylum." + "\n" + "And part of the " +kingdom+ " kingdom."); }

    @Override
    public void eat(){ System.out.println(name + " loves to eat fish, crab, squid."); }

    @Override
    public void lifeSpan(){ System.out.println(name+ "'s" + " lifespan is between 15 and 25 years."); }

    @Override
    public void speed(){ System.out.println(name + " moves about 27mph."); }

}//end of class Seal
